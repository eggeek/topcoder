#define N 1400
const llong mod = 1000000007L;
llong c[N][N], f[40][N];

void initc() {
    c[0][0] = 1;
    for (int i=1; i<N; i++) {
        c[i][0] = c[i][i] = 1;
        for (int j=1; j<i; j++) c[i][j] = (c[i-1][j-1] + c[i-1][j]) % mod;
    }
}

class LISNumber {
    public:
    int count(vector<int> cnt, int K) {
        initc();
        memset(f, 0, sizeof(f));
        int n = SZ(cnt);
        f[0][cnt[0]] = 1;
        for (int sum=0, i=0; i<n-1; i++) {
            sum += cnt[i];
            for (int j=cnt[i]; j<=K; j++) if (f[i][j]) {
                for (int add=0; add <= cnt[i+1] && j+add <= K; add++) {
                    int rest = cnt[i+1] - add;
                    int pos = sum - (j - 1) + rest;
                    llong inc;
                    inc = f[i][j] * c[pos+add-1][pos-1] % mod * c[j][rest] % mod;
                    f[i+1][j+add] += inc;
                    f[i+1][j+add] %= mod;
                }
            }
            //for (int j=cnt[i+1]; j<=K; j++) printf("f[%d][%d]=%lld\n", i+1, j, f[i+1][j]);
        }
        return f[n-1][K];
    }
};

