class EllysScrabble {
    public:
    string getMin(string letters, int maxDistance) {
        int n = (int)letters.size();
        set<pair<char, int>> s;
        if (maxDistance >= n) {
          sort(ALL(letters));
          return letters;
        } else {
          for (int i=0; i<=maxDistance; i++) s.insert({letters[i], i});
          string res = "";
          for (int i=0; i<n; i++) {
            pair<char, int> it = *s.begin();
            for (auto j: s) {
              if (i == j.second + maxDistance) {
                it = j;
                break;
              } else if (it.first > j.first) it = j;
            }
            s.erase(it);
            res.push_back(it.first);
            if (i+1+maxDistance < n) s.insert({letters[i+1+maxDistance], i+1+maxDistance});
          }
          return res;
        }
    }
};

